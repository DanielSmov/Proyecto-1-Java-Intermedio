package negocio.clases;

/**
 *
 * @author Danny
 */
public class Salon {
    private Integer numSalon;
    private Integer cantCamas;
    private String area;
    private Integer cedulaDoctor;

    public Integer getNumSalon() {
        return numSalon;
    }

    public void setNumSalon(Integer numSalon) {
        this.numSalon = numSalon;
    }

    public Integer getCantCamas() {
        return cantCamas;
    }

    public void setCantCamas(Integer cantCamas) {
        this.cantCamas = cantCamas;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public Integer getCedulaDoctor() {
        return cedulaDoctor;
    }

    public void setCedulaDoctor(Integer cedulaDoctor) {
        this.cedulaDoctor = cedulaDoctor;
    }
    
    
}
